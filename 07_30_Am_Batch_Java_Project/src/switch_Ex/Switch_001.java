package switch_Ex;

public class Switch_001 {

	public static void main(String[] args) {

		int i = 500;
		switch (i) {
		case 100:
			System.out.println("Number is 100");
			break;
		case 200:
			System.out.println("Number is 200");
			break;
		case 500:
			System.out.println("Number is 500");
			break;
		default:
			System.out.println("Number is invalid");
			break;
		}
	}

}
